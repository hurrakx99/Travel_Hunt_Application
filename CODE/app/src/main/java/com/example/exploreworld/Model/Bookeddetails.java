package com.example.exploreworld.Model;

public class Bookeddetails {
    public String address;
    public String adhar_no;
    public String date;
    public String day;
    public String document;
    public String name;
    public String no_of_females;
    public String no_of_males;
    public String package_title;
    public String phone_number;
    public String resort_title;
    public String status;
    public String title_image;
    public String total_person;
    public String total_price;

    public Bookeddetails() {
    }

    public Bookeddetails(String address, String adhar_no, String date, String day, String document, String name, String no_of_females, String no_of_males, String package_title, String phone_number, String resort_title, String status, String title_image, String total_person, String total_price) {
        this.address = address;
        this.adhar_no = adhar_no;
        this.date = date;
        this.day = day;
        this.document = document;
        this.name = name;
        this.no_of_females = no_of_females;
        this.no_of_males = no_of_males;
        this.package_title = package_title;
        this.phone_number = phone_number;
        this.resort_title = resort_title;
        this.status = status;
        this.title_image = title_image;
        this.total_person = total_person;
        this.total_price = total_price;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAdhar_no() {
        return adhar_no;
    }

    public void setAdhar_no(String adhar_no) {
        this.adhar_no = adhar_no;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getDocument() {
        return document;
    }

    public void setDocument(String document) {
        this.document = document;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNo_of_females() {
        return no_of_females;
    }

    public void setNo_of_females(String no_of_females) {
        this.no_of_females = no_of_females;
    }

    public String getNo_of_males() {
        return no_of_males;
    }

    public void setNo_of_males(String no_of_males) {
        this.no_of_males = no_of_males;
    }

    public String getPackage_title() {
        return package_title;
    }

    public void setPackage_title(String package_title) {
        this.package_title = package_title;
    }

    public String getPhone_number() {
        return phone_number;
    }

    public void setPhone_number(String phone_number) {
        this.phone_number = phone_number;
    }

    public String getResort_title() {
        return resort_title;
    }

    public void setResort_title(String resort_title) {
        this.resort_title = resort_title;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTitle_image() {
        return title_image;
    }

    public void setTitle_image(String title_image) {
        this.title_image = title_image;
    }

    public String getTotal_person() {
        return total_person;
    }

    public void setTotal_person(String total_person) {
        this.total_person = total_person;
    }

    public String getTotal_price() {
        return total_price;
    }

    public void setTotal_price(String total_price) {
        this.total_price = total_price;
    }
}
